func main() {

	r := gin.Default()

	// 原始
	r.GET("/str", func(ctx *gin.Context) {
		ctx.String(http.StatusOK, Content)
	})

	// 使用套件的gzip
	r.GET("/str2", func(ctx *gin.Context) {
		ctx.String(http.StatusOK, Content)
	}, ggzip.Gzip(ggzip.DefaultCompression))

	// 自己接收gzip的request以及response
	r.GET("/", func(ctx *gin.Context) {
		// request
		r, err := gzip.NewReader(ctx.Request.Body)
		if err != nil {
			_ = ctx.AbortWithError(http.StatusBadRequest, err)
			return
		}
		ctx.Request.Header.Del("Content-Encoding")
		ctx.Request.Header.Del("Content-Length")
		ctx.Request.Body = r

		// response
		ctx.Writer.Header().Set("Content-Encoding", "gzip")
		g := gzip.NewWriter(ctx.Writer)
		defer g.Close()
		g.Write([]byte("Hello world!!"))
	})

	r.Run()
}
